<div class="configure" id="productcountdowntimers-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#productcountdowntimerspg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">CONTENT</span> <span class="pre">&gt;</span> <span class="pre">Pages</span></code>
    <br/>
    Add new page or edit an exist page, then scroll to section <strong>Content</strong> and click to Edit with Page Builder. Add <strong>Product Countdown Timers</strong> like we have instructed in <a class="linkinpage" href="#productcountdowntimerspg-howToUse">How to Use</a>.
    </p>
    <img src="images/productcountdowntimerapper.png" alt="" width="90%">
    <p>There are 2 appearance types: Grid/List and Carousel.
    </p>
    <ul class="normal">
        <img src="images/productcountdowntimersetting.png" alt="" width="70%">
        
        <li>
            <p>In the <strong>Select Products</strong> field: Choose the products you want to add countdown timer in. Hold the <strong>Ctrl</strong> button to choose multi products.</p>
        </li>
        <li>
            <p>In the <strong>Layout</strong> field: Select the design of Product Countdown Timers Layout.</p>
        </li>
        <li>
            <p>In the <strong>Title</strong> field: Please specify title you want to display on the frontend. You can leave it blank if necessary.</p>
        </li>
        <li>
            <p>In the <strong>Short description</strong> field: Please specify short description you want to display on the frontend. You can leave it blank if necessary.</p>
        </li>
        <li>
            <p>In the <strong>Space Between Item</strong> field: Specify the space between items.</p>
        </li>
    </ul>
    <h3>Countdown</h3>
    <ul class="normal">
        <img src="images/countdownsetting.png" alt="" width="70%">
        
        <li>
            <p>In the <strong>Design</strong> field: Select the theme of Countdown box: Dark or Light.</p>
        </li>
        <li>
            <p>In the <strong>Text Day</strong> field: Specify the text of day.</p>
        </li>
        <li>
            <p>In the <strong>Text Hour</strong> field: Specify the text of hour.</p>
        </li>
        <li>
            <p>In the <strong>Text Minute</strong> field: Specify the text of minute.</p>
        </li>
        <li>
            <p>In the <strong>Text Second</strong> field: Specify the text of second.</p>
        </li>
    </ul>
