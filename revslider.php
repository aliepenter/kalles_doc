    <h3 class="title" id="revsliderpg-howToConfigure-3">
        3. Using Slider
        <a href="#revsliderpg-howToConfigure-3" class="hyperlink"></a>
    </h3>
    <img src="images/slimport.png" width="90%" alt="">
