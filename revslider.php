    <br/>
    Add new page or edit an exist page, then scroll to section <strong>Content</strong> and click to Edit with Page Builder. Drag and drop a <strong>Row</strong>, then drag and drop <strong>Text</strong> item on this row. Click to button edit to Call Slider Revolution.
    </p>
    <img src="images/slinserttopage.png" width="70%" alt="">
    <p>Select <strong>NWDTHEMES Slider Revolution</strong> widget and modify as you need. Then click <strong>Save Config</strong> to save. Reload your site and you can see the result.</p>
