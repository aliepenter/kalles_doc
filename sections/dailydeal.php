    <h3 class="title" id="dailydealpg-howToConfigure-2">
        2. Carousel Settings
        <a href="#dailydealpg-howToConfigure-2" class="hyperlink"></a>
    </h3>
    <ul class="normal">
        <img src="images/carouselsetting.png" alt="" width="80%">
        <li>
            <p>In the <strong>Carousel Mode</strong> field: Determine how the products are displayed within the carousel.</p>
        </li>
        <li>
            <p>In the <strong>Autoplay</strong> field: When autoplay is enabled, the carousel begins scrolling automatically when the page loads. If you leave the default setting (No), the customer must click the slide navigation (dots or arrows) to display each slide in sequence.</br>If you enable this feature, enter <strong>Autoplay Speed</strong> to specify the delay in milliseconds between each slide. The default value is 4000 ms (4 seconds).</p>
        </li>
        <li>
            <p>In the <strong>Infinity Loop</strong> field: When infinite loop is enabled, the slide show replays indefinitely while the page is open. If you leave the default setting (No), the slide show plays only once.</br> Be aware that if you set <strong>Infinite Loop</strong> to No and <strong>Autoplay</strong> set to Yes, the autoplay will stop at the end of the number of products to be displayed.</p>
        </li>
        <li>
            <p>In the <strong>Show Arrows</strong> field: When this is enabled, each slide includes next and previous navigation arrows on the left and right side. If you leave the default setting (No), the slides do not display navigation arrows.</p>
        </li>
        <li>
            <p>In the <strong>Show Dots</strong> field: When set to the default setting (Yes), navigation dots appear at the bottom of the carousel slider. If you disable this setting, the carousel slider does not display navigation dots.</p>
        </li>
        <li>
            <p>In the <strong>Rows</strong> field: Specify the row of carousel. Setting this to more than 1 initializes grid mode.</p>
        </li>
        <li>
            <p>In the <strong>Margin</strong> field: Specify Margin Right on item.</p>
        </li>
    </ul>
