<div class="configure" id="themedesignpg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#themedesignpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Theme Design</span></code>.</p>
    <h3 class="title" id="themedesignpg-howToConfigure-1">
        1. General
        <a href="#themedesignpg-howToConfigure-1" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tdgeneral.png" width="90%" alt="">
        <li>
            <p>In the <strong>Disable Debug Mode</strong> field: Enable or disable Debig Mode. It will break the style and It is used only for developer to debug on frontstore.</p>
        </li>
        <li>
            <p>In the <strong>Page Width</strong> field: Specify your frontstore max width.</p>
        </li>
        <li>
            <p>In the <strong>Icon LazyLoad</strong> field: Upload the Lazy Loading icon.</p>
        </li>
        <li>
            <p>In the <strong>Button Border Radius</strong> field: Specify the border radius of button.</p>
        </li>
    </ul>
    <h3 class="title" id="themedesignpg-howToConfigure-2">
        2. Fonts
        <a href="#themedesignpg-howToConfigure-2" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tdfont.png" width="90%" alt="">
        <li>
            <p>In the <strong>Fetch Google Fonts</strong> field: Click <strong>Submit</strong> to Fetch Google Fonts via API. It allows get data Google Fonts for the available font families. The REST API supplies data in the JSON format that includes the styles and scripts (called subsets in Google Fonts) in each family.</p>
        </li>
        <li>
            <p>In the <strong>Body Font Family</strong> field: Select type of Font to use in Site.</p>
        </li>
        <li>
            <p>In the <strong>Customize</strong> field: Customize the font. Please follow this <a class="linkinpage" href="https://fonts.google.com/specimen/Poppins">link</a> for more information.</p>
        </li>
        <li>
            <p>In the <strong>Body Font Size</strong> field: Specify Body Fontsize.</p>
        </li>
        <li>
            <p>In the <strong>Body Font Weight</strong> field: Specify Body Fontweight.</p>
        </li>
        <li>
            <p>In the <strong>Heading Font Family</strong> field: Select Font family of Heading.</p>
        </li>
        <li>
            <p>In the <strong>Menu Font Family</strong> field: Select Font family of menu.</p>
        </li>
        <li>
            <p>In the <strong>Other Font Family	</strong> field: Select orther Font Family.</p>
        </li>
    </ul>
    <h3 class="title" id="themedesignpg-howToConfigure-3">
        3. Colors
        <a href="#themedesignpg-howToConfigure-3" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tdcolor.png" width="90%" alt="">
        <li>
            <p>In the <strong>Theme Colors</strong> field: Select your custom color to the theme.</p>
            <img src="images/tsfooterblock.png" width="90%" alt="">
        </li>
        <li>
            <p>In the <strong>Custom</strong> field of <strong>Basic Colors</strong>: Select <strong>Yes</strong> to customize Basic Colors as below.</p>
            <img src="images/tdbasiccolors.png" width="70%" alt="">
        </li>
        <li>
            <p>In the <strong>Custom</strong> field of <strong>Button Colors</strong>: Select <strong>Yes</strong> to customize Button Colors as below.</p>
            <img src="images/tdbuttoncolor.png" width="70%" alt="">
        </li>
    </ul>
    <h3 class="title" id="themedesignpg-howToConfigure-4">
        4. Header
        <a href="#themedesignpg-howToConfigure-4" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tdheader.png" width="90%" alt="">
        <li>
            <p>In the <strong>Sticky Background Color</strong> field: Select <strong>Default</strong> if you want to use our default color for sticky header. Select <strong>Custom</strong> to customize color for sticky header.</p>
        </li>
        <li>
            <p>In the <strong>Custom Header</strong> field: Select <strong>Yes</strong> if you want to Customize some options of header as below.</p>
