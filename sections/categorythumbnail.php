<div class="overview" id="categorythumbnailpg-overview">
    <h2 class="title">
        Overview
        <a href="#categorythumbnailpg-overview" class="hyperlink"></a>
    </h2>
    <p>
        Blueskytechco Page Builder provides a lot of functions for creating content by dragging-and-dropping pre-built controls.<br/>
        One of them is Category Thumbnail Module. It is also a module for displaying categories, but this makes it available in many different templates, with many different customizations. The following information will guide you how to work with Page Builder - Category Thumbnail.
    </p>
