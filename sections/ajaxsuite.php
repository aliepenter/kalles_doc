<div class="configure" id="ajaxsuitepg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#ajaxsuitepg-howToConfigure" class="hyperlink paLink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Ajax Suite</span></code>.</p>
    <h3 class="title" id="ajaxsuitepg-howToConfigure-1">
        1. General Options
        <a href="#ajaxsuitepg-howToConfigure-1" class="hyperlink chiLink"></a>
    </h3>
    <ul class="list-menu normal">
        <li>
            <h4>General Options</h4>
            <img src="images/Ajgeneral.png" width="80%" alt="">
            <p>In the <strong>Enabled AjaxSuite</strong> field: Select <strong>Yes</strong> if you want to enable <strong>Ajax Suite</strong> module.</p>
        </li>
        <li>
            <h4>Ajax Cart</h4>
            <img src="images/AjAjaxCart.png" width="80%" alt="">
            <ul>
                <li><p>In the <strong>Enable</strong> field: Select <strong>Yes</strong> if you want to enable <strong>Ajax Cart</strong>.</p></li>
                <li>
                    <p>In the <strong>Action after Add to cart Success</strong> field:</p>
                    <ul>
                        <li>
                            <p><strong>Popup</strong>: After add to cart, an information popup will be shown.</p>
                            <img src="images/popup.png" width="80%" alt="">
                        </li>
                        <li>
                            <p><strong>Show Minicart</strong>: After add to cart, Minicart will be shown.</p>
                            <img src="images/showminicart.png" width="80%" alt="">
                        </li>
                    </ul>
                </li>
            </ul>
        </li>
        <li>
            <h4>Ajax Wishlist</h4>
            <img src="images/Ajwishlist.png" width="80%" alt="">
            <ul>
                <li>
                    <p>In the <strong>Enable</strong> field: Select <strong>Yes</strong> if you want to enable <strong>Ajax Cart</strong>.</p>
                </li>
                <li>
                    <p>In the <strong>Title</strong> field: Insert the Title of login popup.</p>
                </li>
                <li>
                    <p>In the <strong>Description</strong> field: Insert the Description of login popup.</p>
                </li>
                <li>
                    <p>In the <strong>Login Popup Logo</strong> field: Setup the Logo of login popup.</p>
                </li>
                <li>
                    <p>In the <strong>Background Image Popup</strong> field: Insert the Background of login popup.</p>
                </li>
            </ul>
            <img src="images/AjFewishlish.png" width="80%" alt="">
        </li>
        <li>
            <h4>Ajax Compare</h4>
            <img src="images/AjajaxCompare.png" width="80%" alt="">
            <p>In the <strong>Enable</strong> field: Select <strong>Yes</strong> if you want to enable <strong>Ajax Compare</strong>.</p>
        </li>
    </ul>
