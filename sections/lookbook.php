<div class="configure" id="lookbookpg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#lookbookpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">CONTENT</span> <span class="pre">&gt;</span> <span class="pre">Pages</span></code>
    <br/>
    Add new page or edit an exist page, then scroll to section <strong>Content</strong> and click to Edit with Page Builder. Add <strong>Lookbook</strong> like we have instructed in <a class="linkinpage" href="#lookbookpg-howToUse">How to Use</a>.
    </p>
    <img src="images/lookbookapper.png" alt="" width="90%">
    <p>There are 4 appearance types: Banner, Grid/List, Carousel and Mansoy.
    </p>
    <ul class="normal">
        <img src="images/lookbooksetting.png" alt="" width="70%">
        
        <li>
            <p>In the <strong>Select Items</strong> field: Choose the items which be added in <a class="linkinpage" href="#imageboxpg-howToConfigure">Blueskytechco - Lookbook</a> to show in lookbook</p>
        </li>
        <li>
            <p>In the <strong>Title</strong> field: Please specify title you want to display on the frontend. You can leave it blank if necessary.</p>
        </li>
        <li>
            <p>In the <strong>Short description</strong> field: Please specify short description you want to display on the frontend. You can leave it blank if necessary.</p>
        </li>
        <li>
            <p>In the <strong>Open In</strong> field: It will show quickview porduct or pupup product.</p>
        </li>
        <li>
            <p>In the <strong>Image Hover Effects</strong> field: Select Image Hover Effects.</p>
        </li>
        <li>
            <p>In the <strong>Background Color</strong> field: Spectify the color of background.</p>
        </li>
        <li>
            <p>In the <strong>Text Color</strong> field: Specify the color of text.</p>
        </li>
    </ul>
