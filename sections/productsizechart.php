<div class="overview" id="productsizechartpg-overview">
    <h2 class="title">
        Overview
        <a href="#productsizechartpg-overview" class="hyperlink"></a>
    </h2>
    <p>
        To make it more convenient for customers to choose the size of their products, store owners can add size charts by using <strong>Product Size Charts</strong> module.
    </p>
</div>
<div class="use">
    <h2 class="title" id="productsizechartpg-howToUse">
        1. Customers
        <a href="#productsizechartpg-howToUse" class="hyperlink"></a>
    </h2>
    <p>In Detail Page, Customers can see the <strong>Size Chart</strong> by click to <strong>Size Guide</strong> button.
    <video autoplay muted loop playsinline __idm_id__="516097" style="width:100%">
        <source src="images/videos/size guide.webm" type="video/webm">
    </video>
</div>
<div class="configure" id="productsizechartpg-howToConfigure">
    <h2 class="title">
        2. Admin
        <a href="#productsizechartpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Product Size Charts</span></code>. Click to <strong>Add New</strong> to add a Size Chart
    </p>
    <img src="images/scpath.png" alt="" width="90%">
    <ul class="normal">
        <img src="images/scsetting.png" alt="" width="70%">
        
        <li>
            <p>In the <strong>Name</strong> field: Specify the name of <strong>Size Chart</strong>.</p>
        </li>
        <li>
            <p>In the <strong>Type</strong> field: Select the type of <strong>Size Chart</strong>.</p>
        </li>
        <li>
            <p>In the <strong>Category</strong> field: Add the category to <strong>Size Chart</strong>.</p>
        </li>
        <img src="images/scpb.png" alt="" width="70%">
        <li>
            <p>Click to <strong>Edit with Page Builder</strong> to customize your size chart content.</p>
        </li>
    </ul>
    <p>After create a new <strong>Size Chart</strong>, you need to add it to products. Go to <code class=""><span class="pre">CATALOG</span> <span class="pre">&gt;</span> <span class="pre">Products</span></code>, choose the product you want to add size chart to, scroll to <strong>Product Size Guide</strong> and choose the <strong>Size Chart</strong>
    </p>
    <img src="images/scpro.png" alt="" width="70%">
    <p>Click <strong>Save</strong> to save the config.</p>
</div>