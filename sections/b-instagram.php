    <h2 class="title">
        How to Configure
        <a href="#b-instagrampg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Instagram</span></code>.</p>
    <h3 class="title" id="b-instagrampg-howToConfigure-1">
        1. General
        <a href="#b-instagrampg-howToConfigure-1" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/instagramsetting.png" width="90%" alt="">
        <li>
            <p>In the <strong>Enable</strong> field: Select <strong>Yes</strong> to enable module Instagram</p>
        </li>
        <li>
            <p>In the <strong>User Id</strong> field: Insert your User Id</p>
        </li>
        <li>
            <p>In the <strong>Access Token</strong> field: Insert your Access Token</p>
        </li>
        <li>
            <p>In the <strong>Username</strong> field: Insert your Username</p>
        </li>
        <li>
            <p>In the <strong>Number of Photos</strong> field: Insert your Number of Photos you want to show.</p>
        </li>
        <li>
            <p>In the <strong>Fetch Posts</strong> field: Click to <strong>Submit</strong> to Fetch posts from instagram.</p>
        </li>
        <p>Click <strong>Save Config</strong> if you see a success box after click.</p>
        <img src="images/instagramsuccess.png" width="90%" alt="">
    </ul>
