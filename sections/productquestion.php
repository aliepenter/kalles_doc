<div class="use">
    <h2 class="title" id="productquestionpg-howToUse">
        1. Customers
        <a href="#productquestionpg-howToUse" class="hyperlink"></a>
    </h2>
    <p>In Detail Page, Customers can send to the store questions by click to <strong>Ask a Question</strong> button. Fill all the fields of <strong>Ask a Question</strong> popup and click to <strong>Send</strong>. After sending a question, customers will be received an email.</p>
    <img src="images/qinpage.png" alt="" width="60%">
    <img src="images/qques.png" alt="" width="60%">
</div>
<div class="configure" id="productquestionpg-howToConfigure">
    <h2 class="title">
        2. Admin
        <a href="#productquestionpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Product Questions</span></code>. Click to <strong>Manage Questions</strong> to manage all customers's questions
    </p>
    <img src="images/qpath1.png" alt="" width="90%">
    <img src="images/qlist.png" alt="" width="90%">
    <p>Go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Product Questions</span></code>. Click to <strong>Setting</strong> to setup <strong>Product Questions</strong> module
    </p>
    <img src="images/qpath2.png" alt="" width="90%">
    <ul class="normal">
        <img src="images/qsetting.png" alt="" width="70%">
        
        <li>
            <p>In the <strong>Enable</strong> field: Select <strong>Enable</strong> to enable <strong>Product Questions</strong> module.</p>
        </li>
        <li>
            <p>In the <strong>Email received</strong> field: Specify the email which will be received messages from customers.</p>
        </li>
        <li>
            <p>In the <strong>Email Template</strong> field: Specify the template of email.</p>
        </li>
    </ul>
