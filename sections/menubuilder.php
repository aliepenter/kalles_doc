    <h4 id="menubuilderpg-howToConfigure-1-2" class="title">
        1.2 Customize Menu in Page
        <a href="#menubuilderpg-howToConfigure-1-2" class="hyperlink"></a>
    </h4>
    <p>If you want to add a vertical menu in your content of page as the following picture, <strong>MenuBuilder</strong> module provides you an easy way to do that.</p>
    <img src="images/frontMenuinpage.png" width="90%" alt="">
    <ul class="normal">
        <li>
            <p>Login to your backend page, then go to&nbsp;<code class=" "><span class="pre ">CONTENT</span> <span class="pre ">&gt;</span> <span class="pre ">Pages</span></code></p>
        </li>
        <li>
            <p>Select the page you want to add menu, then click <strong>Edit</strong>, or click <strong>Add New Page</strong> if you want to create a new page.</p>
        </li>
        <li>
            <p>Select <code class=" "><span class="pre ">Content</span> <span class="pre ">&gt;</span> <span class="pre ">Edit with Page Builder</span></code>, the Page Builder section will appear.</p>
            <img src="images/editpagebuilder.png" width="90%" alt="">
        </li>
        <li>
            <p>Drag Row then drag a Text inside it. Click to the Text then choose <strong>Insert Widget</strong>.</p>
            <img src="https://i.imgur.com/ywV1YRq.gif" width="90%" alt="">
        </li>
        <li>
            <p>In the <strong>Insert Widget</strong> section, choose <strong>Menus Page Builder</strong> widget then fill all the field as you need.</p>
            <img src="images/insertMenuWidget.png" width="60%" alt="">
        </li>
        <li>
            <p>After completing these steps, click <strong>Save</strong> to save your configuration. Then go to <code class=" "> <span class="pre ">ROKANTHEMES</span> <span class="pre ">&gt;</span> <span class="pre ">MenuBuilder</span> <span class="pre ">&gt;</span> <span class="pre ">Menus Setting</span> <span class="pre ">&gt;</span> <span class="pre ">Config Vertical Menu</span></code>                                    to make sure that you disable the Vertical menu.</p>
            <img src="images/disableverticalmenu.png" width="50%" alt="">
        </li>
        <li>
            <p>Go to your frontstore and you can see the result.</p>
            <img src="images/MenuInPage.png" width="90%" alt="">
        </li>
    </ul>
    <h3 class="title" id="menubuilderpg-2">
        2. Menu Setting
        <a href="#menubuilderpg-2" class="hyperlink"></a>
    </h3>
    <p>In this option, you can configure more about the menu.</p>
    <h4 id="menubuilderpg-howToConfigure-2-1" class="title">
        2.1 Config Horizontal Menu
        <a href="#menubuilderpg-howToConfigure-2-1" class="hyperlink"></a>
    </h4>
    <img src="images/horizontalmenuconfig.png" width="50%" alt="">
    <ul class="normal">
        <li>
            <p>In the <strong>Enabled</strong> field: Select <strong>Yes</strong> if you want to enable Horizontal Menu</p>
        </li>
        <li>
            <p>In the <strong>Horizontal Menu</strong> field: Select the menus which created in <strong>Menu Builder</strong></p>
        </li>
        <li>
            <p>In the <strong>Dropdowms Animation</strong> field: Select animation for Dropdown submenu.</p>
            <img src="images/dropdownanimation.png" width="60%" alt="">
        </li>
    </ul>
    <h4 id="menubuilderpg-howToConfigure-2-2" class="title">
        2.2 Config Vertical Menu
        <a href="#menubuilderpg-howToConfigure-2-2" class="hyperlink"></a>
    </h4>
    <img src="images/verticalmenuconfig.png" width="50%" alt="">
    <ul class="normal">
        <li>
            <p>In the <strong>Enabled</strong> field: Select <strong>Yes</strong> if you want to enable Vertical Menu</p>
        </li>
        <li>
            <p>In the <strong>Title</strong> field: Set the title for your Vertical Menu</p>
        </li>
        <li>
            <p>In the <strong>Veritcal Menu</strong> field: Select the menus which created in <strong>Menu Builder</strong></p>
        </li>
        <li>
            <p>In the <strong>Limit show items</strong> field: Set number of submenu items will be shown in Veritcal Menu. Excess items will be included in <strong>Hide Menus</strong></p>
        </li>
        <li>
            <p>In the <strong>Limit show items by screen</strong> field: Select <strong>Yes</strong> and set number of submenu items in small desktop if you want customize limit show items by screen</p>
            <img src="images/llimititemscreen.png" width="50%" alt="">
        </li>
        <li>
            <p>In the <strong>Dropdowms Animation</strong> field: Select animation for Dropdown submenu.</p>
            <img src="images/dropdownanimation2.png" width="50%" alt="">
        </li>
    </ul>
