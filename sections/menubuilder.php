<div class="configure" id="menubuilderpg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#menubuilderpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">MenuBuilder</span></code></p>
    <h3 class="title" id="menubuilderpg-howToConfigure-1">
        1. Menu Builder
        <a href="#menubuilderpg-howToConfigure-1" class="hyperlink"></a>
    </h3>
    <p>In this option, you can show, create, edit, delete menus.</p>
    <h4 class="title" id="menubuilderpg-howToConfigure-1-1">
        1.1 Create a Menu
        <a href="#menubuilderpg-howToConfigure-1-1" class="hyperlink"></a>
    </h4>
    <p>Click to the <strong>Add New Menu</strong> button to direct to create page.</p>
    <img src="https://i.imgur.com/u2UUq39.gif" alt="" width="90%">
    <ul class="list-menu">
        <li>
            <p><i class="far fa-circle"></i>&nbsp;<strong>Menu Name:</strong>&nbsp;Setting name of the menu. <span style="color: red;">*</span></p>
        </li>
        <li>
            <p><i class="far fa-circle"></i>&nbsp;<strong>Menu Type:</strong>&nbsp;Setting type of the menu. There are two types of menu: Veritcal and Horizontal.<span style="color: red;">*</span></p>
        </li>
    </ul>
    <img src="https://i.imgur.com/OBo33oN.gif" alt="" width="90%">
    <p>After menu created. We need to add Cms Page, Categories or Custom Links to <strong>Menu structure</strong>.</p>
    <ul>
        <li>
            <p><strong>Add menu items</strong></p>
            <p>There are three types of menu items you can add: Cms Page, Categories or Custom Links</p>
            <p>Select items you want to add then click to <strong>Add to Menu</strong> button. All the items you have just selected will be called to layout <strong>Menu Structure</strong> in the right.</p>
            <img src="https://i.imgur.com/o3XfJGE.gif" width="100%" alt="">
            <table>
                <tr>
                    <td><img class="img-inline" src="images/cmspagemenu.png" width="100%" alt=""></td>
                    <td><img class="img-inline" src="images/catemenu.png" width="100%" alt=""></td>
