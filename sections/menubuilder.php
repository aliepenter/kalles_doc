                </tr>
            </table>
        </li>
        <li>
            <p><strong>Menu structure</strong></p>
            <p>All the items you just selected will appear here as tags. You can drag and drop those item tags to move the position, determine parent menus, submenus and setting more options for each menus.</p>
            <img src="https://i.imgur.com/9NZEjiP.gif" width="50%" alt="">
            <ul>
                <li>
                    <p><strong>Setting menu item</strong></p>
                    <img src="images/settingitemmeni.png" width="30%" alt="">
                    <ul class="list-menu">
                        <li>
                            <p><i class="far fa-circle"></i>In the &nbsp;<strong>Title</strong>&nbsp;field: You can rename the title of the menu.</p>
                        </li>
                        <li>
                            <p><i class="far fa-circle"></i>In the &nbsp;<strong>Add Class</strong>&nbsp;field: You can add your custom class to each menu.</p>
                        </li>
                        <li>
                            <p><i class="far fa-circle"></i>In the &nbsp;<strong>Add Label</strong>&nbsp;field: There are three types of label: New, Sale and Hot</p>
