<div class="configure" id="iconboxpg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#imageboxpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">CONTENT</span> <span class="pre">&gt;</span> <span class="pre">Pages</span></code>
    <br/>
    Add new page or edit an exist page, then scroll to section <strong>Content</strong> and click to Edit with Page Builder. Add <strong>Image Box</strong> like we have instructed in <a class="linkinpage" href="#imageboxpg-howToUse">How to Use</a>.
    </p>
    <ul class="normal">
        <img src="images/imageboxsetting.png" alt="" width="70%">
        <li>
            <p>In the <strong>Image Position</strong> field: Select position of image.</p>
        </li>
        <li>
            <p>In the <strong>Title</strong> field: Specify title you want to display on the frontend. You can leave it blank if necessary.</p>
        </li>
        <li>
            <p>In the <strong>Short description</strong> field: Specify short description you want to display on the frontend. You can leave it blank if necessary.</p>
        </li>
        <li>
            <p>In the <strong>Image</strong> field: Upload the image to the image box. If you want to use images from gallery, click <strong>Select from Gallery</strong></p>
        </li>
        <li>
            <p>In the <strong>Link</strong> field: Specify the link of the image box.</p>
        </li>
    </ul>
