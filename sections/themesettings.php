    <img src="images/themesettingpath.png" alt="" width="90%">
</div>
<div class="configure" id="themesettingspg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#themesettingspg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Theme Settings</span></code>.</p>
    <h3 class="title" id="themesettingspg-howToConfigure-1">
        1. General
        <a href="#themesettingspg-howToConfigure-1" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tsgeneral.png" width="90%" alt="">
        <li>
            <p>In the <strong>Default Banner</strong> field: Upload the Default Banner of the theme.</p>
        </li>
        <li>
            <p>In the <strong>Back To Top</strong> field: Select <strong>Enable</strong> to enable the <strong>Back to top</strong> button.</p>
        </li>
    </ul>
    <h3 class="title" id="themesettingspg-howToConfigure-2">
        2. Header
        <a href="#themesettingspg-howToConfigure-2" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tsheader.png" width="90%" alt="">
        <li>
            <p>In the <strong>Header Type</strong> field: We create 8 types of header. Select one of them to set header of your frontstore.</p>
        </li>
        <li>
            <p>In the <strong>Sticky Header</strong> field: Select <strong>Enable</strong> to enable the <strong>Sticky Header</strong>.</p>
        </li>
        <li>
            <p>In the <strong>Add Section To Top Header</strong> field: Select section to top of header. There are a lot of blocks that you can use. Please see the following image.</p>
             <img src="images/tsheadersection.png" width="90%" alt="">
        </li>
        <li>
            <p>In the <strong>Enable TopBar</strong> field: Select <strong>Yes</strong> to enable the <strong>TopBar</strong>.</p>
        </li>
    </ul>
    <h3 class="title" id="themesettingspg-howToConfigure-3">
        3. Footer
        <a href="#themesettingspg-howToConfigure-3" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tsfooter.png" width="90%" alt="">
        <li>
            <p>In the <strong>Footer Block</strong> field: Select block for the footer. There are multiple blocks for you to choose.</p>
            <img src="images/tsfooterblock.png" width="90%" alt="">
        </li>
        <li>
            <p>In the <strong>Enable Flex Menu Mobile</strong> field: Select <strong>Yes</strong> to enable flex menu on mobile.</p>
            <img src="images/tsmobilefooter.png" width="50%" alt="">
        </li>
    </ul>
    <h3 class="title" id="themesettingspg-howToConfigure-4">
        4. Category
        <a href="#themesettingspg-howToConfigure-4" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tscate.png" width="90%" alt="">
        <li>
            <p>In the <strong>Product Style</strong> field: Select the style of the product showed on Category Page.</p>
            <img src="images/tscatestyle.png" width="90%" alt="">
        </li>
        <li>
            <p>In the <strong>Show Product Rating Stars</strong> field: Select <strong>Yes</strong> if you want to show Product Rating Stars on Category Page.</p>
        </li>
        <li>
            <p>In the <strong>Show Product Price</strong> field: Select <strong>Yes</strong> if you want to show Product Price on Category Page.</p>
        </li>
        <li>
            <p>In the <strong>Show Product Quickview</strong> field: Select <strong>Yes</strong> if you want to show Product Quickview on Category Page.</p>
        </li>
        <li>
            <p>In the <strong>Show Product Compare</strong> field: Select <strong>Yes</strong> if you want to show Product Compare on Category Page.</p>
        </li>
        <li>
            <p>In the <strong>Show Product Wishlist</strong> field: Select <strong>Yes</strong> if you want to show Product Wishlist on Category Page.</p>
        </li>
        <li>
            <p>In the <strong>Show Product Add to Cart</strong> field: Select <strong>Yes</strong> if you want to show Product Add to Cart on Category Page.</p>
        </li>
        <li>
            <p>In the <strong>Page Layout</strong> field: Select the layout of Category Page.</p>
        </li>
    </ul>
    <h3 class="title" id="themesettingspg-howToConfigure-5">
        5. Product
        <a href="#themesettingspg-howToConfigure-5" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tsproduct.png" width="90%" alt="">
        <li>
            <p>In the <strong>Show Alternative Product Image</strong> field: Show thumbnail image when mouse over in the product image area.</p>
        </li>
        <li>
            <p>In the <strong>Image Hover Effects</strong> field: Effects when mouse over in the product image area.</p>
        </li>
        <li>
            <p>In the <strong>Main Image Width</strong> field: Image size used for the main image on detail product pages.</p>
        </li>
        <li>
            <p>In the <strong>Small Image Width</strong> field: Image size used for products in the catalog, home page, cart page.</p>
        </li>
        <li>
            <p>In the <strong>Image Cropping</strong> field: If you select <strong>1:1</strong> Images will be cropped into a square, if you select <strong>Custom</strong> Images will be cropped to a custom aspect ratio.</p>
        </li>
        <img src="images/tsproductlabel.png" width="50%" alt="">
        <li>
            <p>In the <strong>Label "New"</strong> field: Enable the label "New".</p>
        </li>
        <li>
            <p>In the <strong>Label "New" Text</strong> field: Customize the text of label "New".</p>
        </li>
        <li>
            <p>In the <strong>Label "Sale"</strong> field: Enable the label "Sale".</p>
        </li>
        <li>
            <p>In the <strong>Label "Sale" with Discount Percent</strong> field: Select <strong>Yes</strong> if you want to show Discount Percent.</p>
        </li>
        <img src="images/tsproduct2.png" width="90%" alt="">
        <li>
            <p>In the <strong>Page Layout</strong> field: Select the layout of <strong>Detail Page</strong>.</p>
        </li>
        <li>
            <p>In the <strong>Custom Tabs</strong> field: Add or modify the Tabs in Detail Page.</p>
        </li>
        <li>
            <p>In the <strong>Big Desktops</strong> field: Specify the column of Related, Up-Sells and Cross-Sells Product in Detail Page on Big Desktops screen.</p>
        </li>
        <li>
            <p>In the <strong>Desktops</strong> field: Specify the column of Related, Up-Sells and Cross-Sells Product in Detail Page on Desktops screen.</p>
        </li>
        <li>
            <p>In the <strong>Small Desktops</strong> field: Specify the column of Related, Up-Sells and Cross-Sells Product in Detail Page on Small Desktops screen.</p>
        </li>
        <li>
            <p>In the <strong>Tablet</strong> field: Specify the column of Related, Up-Sells and Cross-Sells Product in Detail Page on Tablet screen.</p>
        </li>
        <li>
            <p>In the <strong>Mobile</strong> field: Specify the column of Related, Up-Sells and Cross-Sells Product in Detail Page on Mobile screen.</p>
        </li>
        <li>
            <p>In the <strong>Small Mobile</strong> field: Specify the column of Related, Up-Sells and Cross-Sells Product in Detail Page on Small Mobile screen.</p>
        </li>
    </ul>
    <h3 class="title" id="themesettingspg-howToConfigure-6">
        6. Blog
        <a href="#themesettingspg-howToConfigure-6" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tsblog.png" width="90%" alt="">
        <li>
            <p>In the <strong>Page Layout</strong> field: Select layout of blog.</p>
        </li>
    </ul>
    <h3 class="title" id="themesettingspg-howToConfigure-7">
        7. Newsletter Popup
        <a href="#themesettingspg-howToConfigure-7" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tsnewsletter.png" width="90%" alt="">
        <li>
            <p>In the <strong>Newsletter Popup</strong> field: Select <strong>Enable on Homepage</strong> or <strong>Enable on All Pages</strong> to enable the Newsletter Popup.</p>
        </li>
        <li>
            <p>In the <strong>Static Block</strong> field: Select Static Block for Newsletter Popup.</p>
        </li>
    </ul>
    <h3 class="title" id="themesettingspg-howToConfigure-8">
       8. Fake Order
        <a href="#themesettingspg-howToConfigure-8" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tsfakeorder.png" width="90%" alt="">
        <li>
            <p>In the <strong>Enable</strong> field: Select <strong>Enable</strong> to enable Fake Order.</p>
        </li>
        <li>
            <p>In the <strong>Seconds Displayed</strong> field: Specify the time to display fake order(second).</p>
        </li>
        <li>
            <p>In the <strong>Seconds Hide</strong> field: Specify the time to hide a fake order(second).</p>
        </li>
        <li>
            <p>In the <strong>Messages</strong> field: Add custom messages to fake order.</p>
        </li>
    </ul>
    <h3 class="title" id="themesettingspg-howToConfigure-9">
        9. Product Popup
        <a href="#themesettingspg-howToConfigure-9" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/tsproductpopup.png" width="90%" alt="">
         <li>
            <p>In the <strong>Product Popup</strong> field: Select <strong>Enable on Homepage</strong> or <strong>Enable on All Pages</strong> to enable the Newsletter Popup.</p>
        </li>
        <li>
            <p>In the <strong>Static Block</strong> field: Select Static Block for Product Popup.</p>
        </li>
        <li>
            <p>In the <strong>Seconds Displayed</strong> field: Specify the time to display Product Popup(second).</p>
        </li>
    </ul>
    <p>After setting, click to <strong>Save Config</strong> to save.</p>
