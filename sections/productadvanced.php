    <h2 class="title">
        How to Configure
        <a href="#productadvancedpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">CONTENT</span> <span class="pre">&gt;</span> <span class="pre">Pages</span></code>
    <br/>
    Add new page or edit an exist page, then scroll to section <strong>Content</strong> and click to Edit with Page Builder. Add <strong>Product Advanced</strong> like we have instructed in <a class="linkinpage" href="#productadvancedpg-howToUse">How to Use</a>.
    </p>
