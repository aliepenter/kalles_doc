<div class="overview" id="productadvancedpg-overview">
    <h2 class="title">
        Overview
        <a href="#productadvancedpg-overview" class="hyperlink"></a>
    </h2>
    <p>
        Blueskytechco Page Builder provides a lot of functions for creating content by dragging-and-dropping pre-built controls.<br/>
        One of them is Product Advanced Module. It is also a module for displaying categories, but this makes it available in many different templates, with many different customizations. The following information will guide you how to work with Page Builder - Product Advanced.
    </p>
</div>
<div class="use">
