<div class="configure" id="optimizespeedpg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#optimizespeedpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Optimize Speed</span></code>.</p>
    <h3 class="title" id="optimizespeedpg-howToConfigure-1">
        1. HTML Optimization
        <a href="#optimizespeedpg-howToConfigure-1" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/optimizehtml.png" width="90%" alt="">
        <li>
            <p>In the <strong>Enable</strong> field: Select <strong>Yes</strong> to enables Minifi HTML.</p>
        </li>
        <li>
            <p>In the <strong>Preload</strong> field: Insert paths for fonts that should be preloaded.
            </p>
        </li>
    </ul>
    <h3 class="title" id="optimizespeedpg-howToConfigure-2">
        2. Lazy Load Image
        <a href="#optimizespeedpg-howToConfigure-2" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/optimizelazyload.png" width="90%" alt="">
        <li>
            <p>In the <strong>Enable Lazy Load</strong> field: Select <strong>Yes</strong> to enable lazy load image.</p>
        </li>
        <li>
            <p>In the <strong>Ignore Class and Id Images</strong> field: Ignore Images that Contain.</p>
        </li>
        <li>
            <p>In the <strong>Ignore Url Images</strong> field: Insert paths for image that should be ignore.
            </p>
        </li>
    </ul>
    <p>Click <strong>Save Config</strong> to save your config.</p>
