<div class="configure" id="onepagecheckoutpg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#onepagecheckoutpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">One Page Checkout</span></code>.</p>
    <h3 class="title" id="onepagecheckoutpg-howToConfigure-1">
        1. General Option
        <a href="#onepagecheckoutpg-howToConfigure-1" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/opcsetting.png" width="90%" alt="">
        <li>
            <p>In the <strong>Enable Express Checkout</strong> field: Select <strong>Yes</strong> to enable module One Page Checkout</p>
        </li>
        <li>
            <p>In the <strong>Seo Url</strong> field: Specify the url of the checkout page.</p>
        </li>
        <li>
            <p>In the <strong>Page Title</strong> field: Specify the title of the checkout page.</p>
        </li>
        <li>
            <p>In the <strong>Short Description</strong> field: Specify short description you want to display on the frontend. You can leave it blank if necessary.</p>
        </li>
        <li>
            <p>In the <strong>Show Order Comment on Checkout</strong> field: Select <strong>Yes</strong> if you want to show on Order Comment checkout.</p>
        </li>
        <li>
            <p>In the <strong>Show Checkbox Subscribe on Checkout</strong> field: Select <strong>Yes</strong> if you want to show Checkbox Subscribe on checkout.</p>
        </li>
        <li>
            <p>In the <strong>Title Box Subscribe Newletter</strong> field: Specify the title of box Subscribe Newletter.</p>
        </li>
        <li>
            <p>In the <strong>Default checkbox Subscribe on Checkout</strong> field: Select <strong>Yes</strong> if you want to set Subscribe checkbox selected by default on checkout.</p>
        </li>
        <li>
            <p>In the <strong>Show Gift Message on Checkout</strong> field: Select <strong>Yes</strong> if you want to show Gift Message on checkout.</p>
        </li>
        <li>
            <p>In the <strong>Show Discount Box on Checkout</strong> field: Select <strong>Yes</strong> if you want to show Discount Box on checkout.</p>
        </li>
        <li>
            <p>In the <strong>Show Widget After Place Order</strong> field: Select <strong>Yes</strong> if you want to show Widget After Place Order on checkout.</p>
        </li>
        <li>
            <p>In the <strong>Show Shipping Delivery Date</strong> field: Select <strong>Yes</strong> if you want to show on Shipping Delivery Date checkout.</p>
        </li>
        <li>
            <p>In the <strong>Title Box Shipping Delivery Date</strong> field: Specify the title of box Shipping Delivery Date.</p>
        </li>
        <img src="images/opcsetting2.png" width="90%" alt="">
        <li>
            <p>In the <strong>Show Shipping Delivery Comment</strong> field: Select <strong>Yes</strong> if you want to show Shipping Delivery Comment on checkout.</p>
        </li>
        <li>
            <p>In the <strong>Title Box Shipping Delivery Comment</strong> field: Specify the title of Box Shipping Delivery Comment.</p>
        </li>
        <li>
            <p>In the <strong>Show full Header and Footer</strong> field: Select <strong>Yes</strong> if you want to show full Header and Footer in checkout page.</p>
        </li>
        <li>
            <p>In the <strong>Title Button Place Order</strong> field: Specify the title of Button Place Order.</p>
        </li>
        <p>Click <strong>Save Config</strong> to save your config.</p>
    </ul>
