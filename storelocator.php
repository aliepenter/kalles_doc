        1. General
        <a href="#storelocatorpg-howToConfigure-1" class="hyperlink"></a>
    </h3>
    <ul class="normal">
         <img src="images/storegapi.png" width="90%" alt="">
        <li>
            <p>In the <strong>Google Api Key</strong> field: Add the Google Api Key here.</p>
        </li>
        <p>Follow this <a href="#">link</a> to know how to get Google Api Key.</p>
    </ul>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Store Locator</span> <span class="pre">&gt;</span> <span class="pre">Manage Stores</span></code>.</p>

    <h3 class="title" id="storelocatorpg-howToConfigure-2">
        Add New Store
        <a href="#storelocatorpg-howToConfigure-2" class="hyperlink"></a>
    </h3>
    <ul class="normal">
        <h4>General Informations</h4>
         <img src="images/storesetting2.png" width="90%" alt="">
        <li>
            <p>In the <strong>Name</strong> field: Insert the name of store. <span style="color: red;">*</span></p>
        </li>
        <li>
            <p>In the <strong>Description</strong> field: Specify short description you want to display on the frontend. You can leave it blank if necessary.</p>
        </li>
        <li>
            <p>In the <strong>Status</strong> field: Select status of the store.
            </p>
        </li>
        <li>
            <p>In the <strong>Address</strong> field: Insert Address of the store.
            </p>
        </li>
        <li>
            <p>In the <strong>City</strong> field: Insert City of the store.
            </p>
        </li>
        <li>
            <p>In the <strong>Zip Code</strong> field: Insert Zip Code of the store area.
            </p>
        </li>
        <li>
            <p>In the <strong>Country</strong> field: Select the Country of the store.
            </p>
        </li>
        <li>
            <p>In the <strong>E-mail</strong> field: Insert E-mail of the store.
            </p>
        </li>
        <li>
            <p>In the <strong>Phone Number</strong> field: Insert Phone Number of the store.
            </p>
        </li>
        <li>
            <p>In the <strong>Fax</strong> field: Insert Fax of the store.
            </p>
        </li>
        <li>
            <p>In the <strong>Website</strong> field: Insert Website of the store.
            </p>
        </li>
        <li>
            <p>In the <strong>Store Image</strong> field: Upload the store thumbnail.
            </p>
        </li>
        <h4>Work Time</h4>
         <img src="images/storeworktime.png" width="90%" alt="">
        <p>Select the day when the store opens.</p>
        <li>
            <p>In the <strong>Open Time</strong> field: Setup the open time.</p>
        </li>
        <li>
            <p>In the <strong>Closed Time</strong> field: Setup the closed time.
            </p>
        </li>
        <h4>Location Map</h4>
         <img src="images/storeloadmap.png" width="90%" alt="">
        <li>
            <p>In the <strong>Latitude</strong> field: Insert the Latitude of the store. <span style="color: red;">*</span></p>
        </li>
        <li>
            <p>In the <strong>Longitude</strong> field: Insert the Longitude of the store. <span style="color: red;">*</span></p>
        </li>
        <li>
            <p>In the <strong>Zoom</strong> field: Insert zoom index.
            </p>
        </li>
        <p>Click <strong>Auto Find</strong> the setup will be success if the store appears in the <strong>Store Location</strong> below.</p>
    </ul>
    <p>Click <strong>Save Store</strong> to save your config.</p>
