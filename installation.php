<div class="configure" id="installationpg-howToConfigure">
    <h2 class="title">
        How to Configure
        <a href="#installationpg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">BLUESKYTECHCO</span> <span class="pre">&gt;</span> <span class="pre">Quick View</span></code>.</p>
    <h3 class="title" id="installationpg-howToConfigure-1">
        1. General Options
        <a href="#installationpg-howToConfigure-1" class="hyperlink"></a>
    </h3>
    <img src="images/settinginstallation.gif" width="90%" alt="">
    <ul class="normal">
        <li>
            <p>In the <strong>Enabled installation</strong> field: Select <strong>Yes</strong> if you want to enable <strong>Quick View</strong> module.</p>
        </li>
        <li>
            <p>In the <strong>Label Item</strong> field: Set the label for the quick view button.</p>
        </li>
        <li>
            <p>In the <strong>Action Append Icon installation</strong> field: Set position of div "installation".</p>
        </li>
        <li>
            <p>In the <strong>Variation of thumbnails in navigation</strong> field: There are two options: Thumbs or Dots.</p>
            <table>
                <tr>
                    <td>
                        <img src="images/thumbinstallation1.png" width="40%" alt="">
                        <img src="images/dotsinstallation.png" width="40%" alt="">
                    </td>
                </tr>
            </table>
        </li>
        <li>
            <p>In the <strong>Sliding direction of thumbnails</strong> field: Position of thumbnail images will be shown.</p>
        </li>
        <li>
            <p>In the <strong>Data or Class Insert</strong> field: Setting class to div "product action".</p>
        </li>
    </ul>
