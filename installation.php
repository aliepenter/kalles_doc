<div class="overview" id="installationpg-overview">
    <h2 class="title">
        Overview
        <a href="#installationpg-overview" class="hyperlink"></a>
    </h2>
    <p>
        Magento 2 by default only allows customers see product detail in product page. This will take time and bring a bad experience for customers when they have to wait for the page to load just to see product details. So <strong>Quick View</strong> module was created to solve this problem. Not only for viewing product details, our module also supports many other functions, making it easy for customers to order the items they are viewing.
    </p>
</div>
