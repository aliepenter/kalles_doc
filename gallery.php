<div class="configure" id="gallerypg-howToConfigure">
<h2 class="title">
        How to Configure
        <a href="#gallerypg-howToConfigure" class="hyperlink"></a>
    </h2>
    <p>Login to your backend page, then go to <code class=""><span class="pre">CONTENT</span> <span class="pre">&gt;</span> <span class="pre">Pages</span></code>
    <br/>
    Add new page or edit an exist page, then scroll to section <strong>Content</strong> and click to Edit with Page Builder. Add <strong>Gallery</strong> like we have instructed in <a class="linkinpage" href="#gallerypg-howToUse">How to Use</a>.
    </p>
    <img src="images/cateThumbSetting.png" alt="" width="70%">
    <p>Step 1. Add new image to gallery. Click to the image box then edit. </p>
    <img src="images/cateThumbSetting.png" alt="" width="70%">
    <ul class="normal">
        <img src="images/cateThumbSetting.png" alt="" width="70%">
        <li>
            <p>In the <strong>Image</strong> field: Upload image. Maximum file size: 4 MB. Allowed file types: JPG, GIF, PNG.</p>
        </li>
        <li>
            <p>In the <strong>Link</strong> field: Add a link to the image to which redirect the customers when they click to the images.</p>
        </li>
        <li>
            <p>In the <strong>Image Caption</strong> field: Specify caption of the image.</p>
        </li>
        <li>
            <p>In the <strong>CSS Classes Custom</strong> field: Add your custom class css. Space separated list of classes</p>
        </li>
    </ul>
    <p>Step 2. Click to the module edit button to configure the Gallery module. </p>
    <img src="images/cateThumbSetting.png" alt="" width="70%">
    <ul class="normal">
        <img src="images/cateThumbSetting.png" alt="" width="70%">
        <li>
            <p>In the <strong>Layout Design</strong> field: Determine how the images are displayed within the galler.</p>
        </li>
        <li>
            <p>In the <strong>Action Link</strong> field: Determine action when customers click to the image.</p>
            <p>a. Photo Swipe</p>
            <img src="images/cateThumbSetting.png" alt="" width="70%">
            <p>a. Open Link</p>
            <img src="images/cateThumbSetting.png" alt="" width="70%">
        </li>
        <li>
            <p>In the <strong>Space Between Item</strong> field: Setup Space between Collections.</p>
            <img src="images/cateThumbSpaceBetween.png" alt="" width="60%">
        </li>
    </ul>
